package Java;

public class Methodoverloading_numberofargument 
{
	int A;
	int B;
public void Numberofargument(int x)
{
A=x;
System.out.println("The square value of A is"+(A*A));
}
public void Numberofargument(int x,int y)
{
	A=x;
	B=y;
	System.out.println("The addition value of A and B is"+(A+B));	
}
}
